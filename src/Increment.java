
public class Increment {
	public static void main(String[] args) {
		int a=10;
		int b=++a;
		int d=6;
		int c=d++;
		int e=a++;
		System.out.println("preincrement="+b);
		System.out.println("postincrement="+c);
		System.out.println("print value d="+d);
		System.out.println("print value e="+e);
	}

}
